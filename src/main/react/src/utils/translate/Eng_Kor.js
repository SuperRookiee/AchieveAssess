const Eng_Kor = (subject) => {
    switch (subject) {
        case 'korean':
            return '국어';
        case 'math':
            return '수학';
        case 'english':
            return '영어';
        case 'social':
            return '사회';
        case 'science':
            return '과학';
    }
}

export default Eng_Kor;